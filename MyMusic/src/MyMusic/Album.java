package MyMusic;

public class Album {
    private String name;
    private String imagePath;




    public String getName() {
        return name;
    }

    public void setName(String n) {
        name = n;
    }

    public String getImagePath() {
        return imagePath;
    }

    public void setImagePath(String path) {
        imagePath = path;
    }
}
